from matplotlib import pyplot as plt
import seaborn as sns

def plot_original_and_forecasts(original_series, dict_with_forecasts):
  palette = sns.color_palette("Paired")
  plt.figure(figsize=(15,7))
  plt.plot(original_series, color='black',label="Original", marker = 'o')
  j=0
  for i in dict_with_forecasts.keys():
    plt.plot(dict_with_forecasts[i], color=palette[j], label=i, marker = 'o')
    j=j+1
  plt.legend(loc='best')
  plt.title('Original value and forecast')

